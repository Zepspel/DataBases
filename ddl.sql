CREATE TABLE region (
	region_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	name VARCHAR(32) NOT NULL UNIQUE,
	description TEXT
);

CREATE TABLE city (
	city_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	region_id INT NOT NULL,
	name VARCHAR(32) NOT NULL,
	description TEXT,
	
	CONSTRAINT fk_city_region
		FOREIGN KEY (region_id)
		REFERENCES region(region_id)
		ON DELETE CASCADE
		ON UPDATE CASCADE
);

CREATE TABLE district (
	district_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	city_id INT NOT NULL,
	name VARCHAR(32) NOT NULL,
	
	CONSTRAINT fk_district_city
		FOREIGN KEY (city_id)
		REFERENCES city(city_id)
		ON DELETE CASCADE
		ON UPDATE CASCADE
);

CREATE TABLE parking (
	parking_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	district_id INT NOT NULL,
	coords POINT,
	
	CONSTRAINT fk_parking_district
		FOREIGN KEY (district_id)
		REFERENCES district(district_id)
		ON DELETE CASCADE
		ON UPDATE CASCADE
);



CREATE TABLE car_class (
	car_class_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	name CHAR(7) CHECK (name in ('econom', 'middle', 'premium', 'family')),
	description TEXT
);

CREATE TABLE price_list (
	car_class_id INT,
	region_id INT,
	cost_min NUMERIC(6,2) CHECK (cost_min >= 0),
	cost_hour NUMERIC(6,2) CHECK (cost_hour >= 0),
	
	CONSTRAINT pk_price_list PRIMARY KEY (car_class_id, region_id),
	
	CONSTRAINT fk_price_list_car_class
		FOREIGN KEY (car_class_id)
		REFERENCES car_class(car_class_id)
		ON DELETE CASCADE
		ON UPDATE CASCADE,
		
	CONSTRAINT fk_price_list_region
		FOREIGN KEY (region_id)
		REFERENCES region(region_id)
		ON DELETE CASCADE
		ON UPDATE CASCADE
);

CREATE TABLE car (
	car_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	class_id INT NOT NULL,
	region_id INT NOT NULL,
	number VARCHAR(32) NOT NULL UNIQUE,
    brand VARCHAR(32) NOT NULL,
    model VARCHAR(32) NOT NULL,
    color VARCHAR(32),
    capacity SMALLINT NOT NULL CHECK (capacity >= 1),
    engine_volume NUMERIC(3,1) CHECK (engine_volume >= 0),
    fuel_consumption NUMERIC(3,1),

    CONSTRAINT fk_car_class
        FOREIGN KEY (class_id)
        REFERENCES car_class (car_class_id)
        ON DELETE RESTRICT
        ON UPDATE CASCADE,

    CONSTRAINT fk_car_region
        FOREIGN KEY (region_id)
        REFERENCES region (region_id)
        ON DELETE RESTRICT
        ON UPDATE CASCADE
);

CREATE TABLE visit_sc (
	visit_sc_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	car_id INT NOT NULL,
	begin_date DATE NOT NULL,
	end_date DATE CHECK (end_date IS NULL OR end_date >= begin_date),
	cause TEXT,
	status TEXT NOT NULL CHECK (status in ('planned', 'in_progress', 'done', 'cancelled')),
	total_cost NUMERIC(11,2),
	
    CONSTRAINT fk_visit_sc_car
        FOREIGN KEY (car_id)
        REFERENCES car (car_id)
        ON DELETE CASCADE
        ON UPDATE CASCADE
);

CREATE TABLE sc (
	sc_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

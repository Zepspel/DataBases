CREATE TABLE card (
    card_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    renter_id INT NOT NULL,
    payment_token TEXT NOT NULL,
    last_digits CHAR(4) NOT NULL,
    duration DATE NOT NULL,
    payment_system VARCHAR(32),

    CONSTRAINT fk_card_renter
        FOREIGN KEY (renter_id)
        REFERENCES renter (renter_id)
        ON DELETE CASCADE
        ON UPDATE CASCADE
);

CREATE TABLE fine (
    fine_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    renter_id INT NOT NULL,
    booking_id INT NOT NULL,
    cause TEXT NOT NULL,
    receiving_time TIMESTAMP NOT NULL,
    payment_time TIMESTAMP,
    amount NUMERIC(11,2) NOT NULL,

    CONSTRAINT fk_fine_renter
        FOREIGN KEY (renter_id)
        REFERENCES renter (renter_id)
        ON DELETE CASCADE
        ON UPDATE CASCADE,

    CONSTRAINT fk_fine_booking
        FOREIGN KEY (booking_id)
        REFERENCES booking (booking_id)
        ON DELETE CASCADE
        ON UPDATE CASCADE,

    CONSTRAINT chk_fine_amount
        CHECK (amount IS NULL OR amount >= 0),

    CONSTRAINT chk_fine_times
        CHECK (payment_time IS NULL OR payment_time >= receiving_time)
);


CREATE TABLE debiting (
    debiting_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    card_id INT NOT NULL,
    booking_id INT NOT NULL,
    time_interval INTERVAL NOT NULL,
    amount NUMERIC NOT NULL,
    type CHAR(4) CHECK (type in ('fine', 'min', 'hour')),

    CONSTRAINT fk_debiting_card
        FOREIGN KEY (card_id)
        REFERENCES card (card_id)
        ON DELETE SET NULL
        ON UPDATE CASCADE,

    CONSTRAINT fk_debiting_booking
        FOREIGN KEY (booking_id)
        REFERENCES booking (booking_id)
        ON DELETE RESTRICT
        ON UPDATE CASCADE,

    CONSTRAINT chk_debiting_amount
        CHECK (amount >= 0)
);


